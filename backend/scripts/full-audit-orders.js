async function getTNOrder(orderNumber, retries = 3) {
  for (let attempt = 0; attempt < retries; attempt++) {
    try {
      const res = await axios.get(
        `https://api.tiendanube.com/v1/${TN_STORE_ID}/orders?q=${orderNumber}`,
        {
          headers: { 'Authentication': `bearer ${TN_TOKEN}` },
          timeout: 10000
        }
      );
      const order = res.data.find(o => String(o.number) === String(orderNumber));
      if (!order) return { not_found: true };
      return {
        payment_status: order.payment_status,
        shipping_status: order.shipping_status,
        status: order.status,
        total: parseFloat(order.total)
      };
    } catch (err) {
      if (attempt < retries - 1) {
        await new Promise(r => setTimeout(r, 2000));
        continue;
      }
      return { error: err.response?.status || err.message };
    }
  }
}

async function main() {
  console.log(`Total pedidos únicos: ${uniqueOrders.length}`);
  console.log('Consultando DB en batches...\n');

  // Get all DB orders in batches to avoid timeout
  const dbMap = {};
  const batchSize = 500;
  for (let i = 0; i < uniqueOrders.length; i += batchSize) {
    const batch = uniqueOrders.slice(i, i + batchSize).map(String);
    const dbResult = await pool.query(`
      SELECT order_number, estado_pago, estado_pedido, monto_tiendanube, total_pagado
      FROM orders_validated
      WHERE order_number = ANY($1)
    `, [batch]);

    for (const row of dbResult.rows) {
      dbMap[row.order_number] = {
        estado_pago: row.estado_pago,
        estado_pedido: row.estado_pedido,
        monto: parseFloat(row.monto_tiendanube),
        pagado: parseFloat(row.total_pagado)
      };
    }
    console.log(`DB batch ${Math.floor(i/batchSize) + 1}/${Math.ceil(uniqueOrders.length/batchSize)}: ${dbResult.rows.length} encontrados`);
  }

  console.log(`\nTotal en DB: ${Object.keys(dbMap).length}`);
  console.log('Consultando Tiendanube...\n');

  const results = [];
  const delay = 500; // 500ms between requests to avoid rate limiting

  for (let i = 0; i < uniqueOrders.length; i++) {
    const orderNum = uniqueOrders[i];
    const db = dbMap[String(orderNum)] || null;
    const tn = await getTNOrder(orderNum);

    results.push({
      order: orderNum,
      db: db,
      tn: tn
    });

    // Progress
    if ((i + 1) % 100 === 0 || i === uniqueOrders.length - 1) {
      const pct = ((i + 1) / uniqueOrders.length * 100).toFixed(1);
      console.log(`Progreso: ${i + 1}/${uniqueOrders.length} (${pct}%)`);
    }

    // Save progress every 500 orders
    if ((i + 1) % 500 === 0) {
      const partialFile = path.join(__dirname, `audit-partial-${i + 1}.json`);
      fs.writeFileSync(partialFile, JSON.stringify(results, null, 2));
      console.log(`  -> Guardado parcial: ${partialFile}`);
    }

    await new Promise(r => setTimeout(r, delay));
  }

  // Save full results
  const outputFile = path.join(__dirname, 'audit-orders-full.json');
  fs.writeFileSync(outputFile, JSON.stringify(results, null, 2));
  console.log(`\nResultados guardados en: ${outputFile}`);

  // Generate summary report
  const summary = {
    total: results.length,
    not_in_db: results.filter(r => !r.db).length,
    not_in_tn: results.filter(r => r.tn.not_found).length,
    tn_errors: results.filter(r => r.tn.error).length,
    synced: 0,
    needs_update: []
  };

  for (const r of results) {
    if (!r.db || r.tn.not_found || r.tn.error) continue;

    // Map TN status to DB status
    const tnPago = r.tn.payment_status === 'paid' ? 'confirmado_total' :
                   r.tn.payment_status === 'pending' ? 'pendiente' :
                   r.tn.payment_status;
    const tnEnvio = ['shipped', 'delivered'].includes(r.tn.shipping_status) ? 'enviado' :
                    r.tn.shipping_status === 'unpacked' ? 'pendiente' :
                    r.tn.shipping_status;

    const pagoOk = r.db.estado_pago === 'confirmado_total' || r.tn.payment_status === 'paid';
    const envioOk = ['enviado', 'retirado', 'en_calle'].includes(r.db.estado_pedido) ||
                    ['shipped', 'delivered'].includes(r.tn.shipping_status);

    if (pagoOk && envioOk && r.db.estado_pago === 'confirmado_total' &&
        ['enviado', 'retirado', 'en_calle'].includes(r.db.estado_pedido)) {
      summary.synced++;
    } else {
      summary.needs_update.push({
        order: r.order,
        db_pago: r.db.estado_pago,
        db_pedido: r.db.estado_pedido,
        tn_pago: r.tn.payment_status,
        tn_envio: r.tn.shipping_status
      });
    }
  }

  // Save summary
  const summaryFile = path.join(__dirname, 'audit-summary.json');
  fs.writeFileSync(summaryFile, JSON.stringify(summary, null, 2));
  console.log(`Resumen guardado en: ${summaryFile}`);

  console.log(`\n=== RESUMEN ===`);
  console.log(`Total: ${summary.total}`);
  console.log(`No están en DB: ${summary.not_in_db}`);
  console.log(`No están en TN: ${summary.not_in_tn}`);
  console.log(`Errores TN: ${summary.tn_errors}`);
  console.log(`Sincronizados (OK): ${summary.synced}`);
  console.log(`Necesitan actualización: ${summary.needs_update.length}`);

  await pool.end();
}

main().catch(err => {
  console.error('Error:', err);
  pool.end();
  process.exit(1);
});
